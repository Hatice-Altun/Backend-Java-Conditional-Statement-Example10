package com.altun.Java;

public class Main {

    public static void main(String[] args) {
        // Write in Java a simple for loop to display numbers 1 to 10.

        System.out.println("Display integers!");

        for (int index = 0; index < 10; index++) {
            int number = index + 1;
            System.out.println("number: " + number);

        }
    }
}
